int	ft_isspecial(char c)
{
	if ((c >= 32 && c <= 47) || (c >= 58 && c <= 64) || (c >= 91 && c <= 94)
		|| (c == 96) || (c >= 123 && c <= 126))
		return (1);
	return (0);
}
