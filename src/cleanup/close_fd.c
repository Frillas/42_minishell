#include "minishell.h"

void	close_fd(int *pipe_fd)
{
	if (pipe_fd != NULL)
	{
		if (pipe_fd[0] > 0)
			close(pipe_fd[0]);
		if (pipe_fd[1] > 0)
			close(pipe_fd[1]);
	}
}
