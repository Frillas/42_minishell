#include "minishell.h"

t_bool	is_operator(char c, int no_space)
{
	if (no_space == 1)
	{
		if ((c == '>') || (c == '<') || (c == '|'))
			return (TRUE);
	}
	else if ((c == ' ') || (c == '>') || (c == '<') || (c == '|'))
		return (TRUE);
	return (FALSE);
}
